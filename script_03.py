s = 'Hello World!'

"""Надрукує "! lo le" — береться зріз рядка з кінця до початку з кроком -2
(половина символів буде пропущено).
"""
print(s[-1: 0: -2]) # Output: !lo le

"""Надрукує "!dlroW olleH" — береться зріз по всьому рядку, але у зворотному
порядку (крок -1).
"""
print(s[: : -1]) # Output: !dlroW olleH

"""Надрукує "llo World" — береться зріз рядка від символу з індексом 2
до останнього невключно.
"""
print(s[2: -1]) # Output: llo World
